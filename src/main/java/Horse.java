public class Horse {
    int name;
    int position;

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public Horse(int name, int position) {
        this.name = name;
        this.position = position;
    }
}
